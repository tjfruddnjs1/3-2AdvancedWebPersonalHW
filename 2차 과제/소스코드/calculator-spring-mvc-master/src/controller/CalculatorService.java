package controller;

public class CalculatorService {

	public double valueSum(double firstValue, double secondValue) {
		return (firstValue+secondValue);
	}
	public double valueSub(double firstValue, double secondValue) {
		return (firstValue-secondValue);
	}
	public double valueMulti(double firstValue, double secondValue) {
		return (firstValue*secondValue);
	}
	public double valueDiv(double firstValue, double secondValue) {
		return (firstValue/secondValue);
	}
}
